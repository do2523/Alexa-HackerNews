class GetTopNewsIntentHandler(AbstractRequestHandler):
    """Handler for GetTopNewsIntent."""
    def can_handle(self, handler_input):
        return ask_utils.is_intent_name("GetTopNewsIntent")(handler_input)

    def handle(self, handler_input):
        res = requests.get('https://news.ycombinator.com/news')
        res2 = requests.get('https://news.ycombinator.com/news?p=2')
        soup_obj = BeautifulSoup(res.text, 'html.parser')
        soup_obj2 = BeautifulSoup(res2.text, 'html.parser')
        links = soup_obj.select('.titleline > a')
        links2 = soup_obj2.select('.titleline > a')
        subtext = soup_obj.select(".subtext")
        subtext2 = soup_obj2.select(".subtext")
        mega_links = links + links2
        mega_subtext = subtext + subtext2

        def sort_stories_by_votes(hnlist):
            return sorted(hnlist, key=lambda k: k['votes'], reverse=True)

        def create_custom_hn(links, subtext):
            hn = []
            for index, item in enumerate(links):
                title = item.getText()
                href = item.get('href', None)
                vote = subtext[index].select('.score')
                if len(vote):
                    points = int(vote[0].getText().replace(' points', ''))
                    if points > 99:
                        hn.append({'title': title, 'link': href, 'votes': points})
            return sort_stories_by_votes(hn)

        news_list = create_custom_hn(mega_links, mega_subtext)
        news_response = "Here are the top news articles: "
        for index, news in enumerate(news_list):
            news_response += f"{index + 1}. {news['title']}. "
        speak_output = news_response

        return (
            handler_input.response_builder
                .speak(speak_output)
                .response
        )
class IntentReflectorHandler(AbstractRequestHandler):
    """The intent reflector is used for interaction model testing and debugging.
    It will simply repeat the intent the user said. You can create custom handlers
    for your intents by defining them above, then also adding them to the request
    handler chain below.
    """
    def can_handle(self, handler_input):
        # type: (HandlerInput) -> bool
        return ask_utils.is_request_type("IntentRequest")(handler_input)

    def handle(self, handler_input):
        # type: (HandlerInput) -> Response
        intent_name = ask_utils.get_intent_name(handler_input)
        speak_output = "You just triggered " + intent_name + "."

        return (
            handler_input.response_builder
                .speak(speak_output)
                # .ask("add a reprompt if you want to keep the session open for the user to respond")
                .response
        )


class CatchAllExceptionHandler(AbstractExceptionHandler):
    """Generic error handling to capture any syntax or routing errors. If you receive an error
    stating the request handler chain is not found, you have not implemented a handler for
    the intent being invoked or included it in the skill builder below.
    """
    def can_handle(self, handler_input, exception):
        # type: (HandlerInput, Exception) -> bool
        return True

    def handle(self, handler_input, exception):
        # type: (HandlerInput, Exception) -> Response
        logger.error(exception, exc_info=True)

        speak_output = "Sorry, I had trouble doing what you asked. Please try again."

        return (
            handler_input.response_builder
                .speak(speak_output)
                .ask(speak_output)
                .response
        )

